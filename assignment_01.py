# 1. Program to Calculate the Area of a circle and triangle 

# area of a circle
r = int(input("Enter the radius of the circle : "))
print("Area of Circle is :",3.14*(r**2))

# area of a triangle
h, b = int(input("Enter Height of the Triangle : ")), int(input("Enter Base of the triangle : "))
print("Area of Triangle is : ",0.5*h*b)
